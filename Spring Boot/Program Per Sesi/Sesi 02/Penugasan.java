public class Penugasan {
    public static void main(String[] args) {
        int x = 12, y = 7;
        x+=y;
        System.out.println("Penambahan: " + x);
        x-=y;
        System.out.println("Pengurangan: " + x);
        x/=y;
        System.out.println("pembagian: " + x);
        x*=y;
        System.out.println("Perkalian: " + x);
    }
}
