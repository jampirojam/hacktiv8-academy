public class Logika {
    public static void main(String[] args) {
        int x = 12, y = 8;
        System.out.println("X == Y (?): " + (x==y));
        System.out.println("X != Y (?): " + (x!=y));
        System.out.println("X >= Y (?): " + (x>=y));
        System.out.println("X <= Y (?): " + (x<=y));
        System.out.println("X > Y (?): " + (x>y));
        System.out.println("X < Y (?): " + (x<y));
    }   
}
